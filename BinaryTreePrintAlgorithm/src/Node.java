
public class Node {
	int value;
	Node leftChild;
	Node rightChild;
	
	/**
	 * @param value
	 * @param leftChild
	 * @param rightChild
	 */
	public Node(int value, Node leftChild, Node rightChild) {
		super();
		this.value = value;
		this.leftChild = leftChild;
		this.rightChild = rightChild;
	}
	
	
	
}
